-- ============================================================
-- МГ-платформа — Миграция 211: поле hero.headline → «Название мастер-группы»
-- Этап 5D:
-- В hero блоке вместо абстрактной «одной фразы о твоём деле»
-- теперь чёткое поле «Название твоей мастер-группы». Сохраняем
-- тот же code='headline' чтобы не ломать page_config у уже
-- опубликованных страниц (данные просто продолжают жить в
-- hero.headline, интерфейс у конструктора и публичной страницы
-- не меняется).
-- ============================================================

update mg_config_questions
set
  label      = 'Название твоей мастер-группы',
  hint       = 'Короткая формулировка: как называется именно твоя мастер-группа. Появится у тебя в хедере страницы под именем.',
  example    = 'Женская гормональная йога',
  required   = true,
  max_length = 80
where code = 'headline';
